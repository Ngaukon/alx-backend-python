#!/usr/bin/env python3
'''This module provides a function to compute the floor of a floating-point number.
'''

def floor(a: float) -> int:
    '''Returns the largest integer less than or equal to the given floating-point number.

    Args:
        a (float): The floating-point number for which to compute the floor value.

    Returns:
        int: The largest integer less than or equal to the input number `a`.
    '''
    return int(a)
