#!/usr/bin/env python3
'''This module provides a function to convert a floating-point number to its string representation.
'''

def to_str(n: float) -> str:
    '''Converts a floating-point number to a string.

    Args:
        n (float): The floating-point number to convert.

    Returns:
        str: The string representation of the input floating-point number.
    '''
    return str(n)
